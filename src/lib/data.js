export function stInfo(s) {
  if (s.qty === 0)        return { cls: 'st-out', txt: 'Sin stock' }
  if (s.qty <= s.min)     return { cls: 'st-out', txt: 'Bajo minimo' }
  if (s.qty <= s.min*1.5) return { cls: 'st-low', txt: 'Poco' }
  return                         { cls: 'st-ok',  txt: 'OK' }
}

export function hoy() {
  return new Date().toISOString().split('T')[0]
}

export function fd(d) {
  if (!d) return '—'
  const [y, m, day] = d.split('-')
  return `${day}/${m}/${y}`
}
